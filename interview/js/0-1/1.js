// if else
function fn(x) {
  if (x === 0) return 1;
  if (x === 1) return 0;
}

// 三元运算符
function fn(x) {
  return x === 0 ? 1 : x === 1 ? 0 : null;
}

// 数组映射 索引
function fn(x) {
  return [1, 0][x];
}

// 异或运算符
function fn(x) {
  return x ^ 1;
}

